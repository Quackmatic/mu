package pw.usn.mu.tokenizer;

/**
 * Represents the type of symbol represented by a {@link SymbolToken}.
 */
public enum SymbolTokenType {
	/**
	 * Represents an opening parenthesis ({@code (}).
	 */
	PAREN_OPEN,
	/**
	 * Represents a closing parenthesis ({@code )}).
	 */
	PAREN_CLOSE
}
